<?php

class CoDescription {
    private $sym;
    private $id;
    private $coDesc;
    public function getSym() {
        return $this->sym;
    }

    public function setSym($sym) {
        $this->sym = $sym;
    }

    public function getId() {
        return $this->id;
    }

    public function setId($id) {
        $this->id = $id;
    }

    public function getCoDesc() {
        return $this->coDesc;
    }

    public function setCoDesc($coDesc) {
        $this->coDesc = $coDesc;
    }


}

?>
